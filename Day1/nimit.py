j=input()
match j:
    case 'a' | 'A':
        print("is a vowel")
    case 'e' | 'E':
        print("is a vowel")
    case 'i' | 'I':
        print("is a vowel")
    case 'o' | 'O':
        print("is a vowel")
    case 'u' | 'U':
        print("is a vowel")
    case _ :
        print("not a vowel")
